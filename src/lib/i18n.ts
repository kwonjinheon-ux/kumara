export function isSupportedLanguage(value: string | null): value is SupportedLanguage {
  return value === "ko" || value === "en" || value === "zh";
}

export function translate(
  language: SupportedLanguage,
  key: keyof typeof translations.ko,
  values: Record<string, string | number> = {},
) {
  const template = String(translations[language][key] ?? translations.ko[key] ?? key);

  return Object.entries(values).reduce(
    (label, [name, value]) => label.replaceAll(`{${name}}`, String(value)),
    template,
  );
}

export function translateMarketValue(language: SupportedLanguage, value: string) {
  return marketValueTranslations[language][value] ?? value;
}

export function formatRelativeTimeLabel(language: SupportedLanguage, value: string, now = Date.now()) {
  const diff = now - new Date(value).getTime();
  const minute = 60 * 1000;
  const hour = 60 * minute;
  const day = 24 * hour;

  if (diff < minute) return translate(language, "time.justNow");
  if (diff < hour) return translate(language, "time.minutesAgo", { count: Math.floor(diff / minute) });
  if (diff < day) return translate(language, "time.hoursAgo", { count: Math.floor(diff / hour) });
  if (diff < day * 2) return translate(language, "time.yesterday");
  if (diff < day * 7) return translate(language, "time.daysAgo", { count: Math.floor(diff / day) });

  return new Intl.DateTimeFormat(getLanguageLocale(language), {
    day: "numeric",
    month: "long",
    year: "numeric",
  }).format(new Date(value));
}

export function getLanguageLocale(language: SupportedLanguage) {
  if (language === "en") return "en-NZ";
  if (language === "zh") return "zh-CN";
  return "ko-KR";
}
